    <h2 class="text-center my-5">Gobierno Autónomo Municipal de La Paz</h2>
    <img src="frontis.jpg" alt="Frontis del Palacio Consistorial" class="img-fluid mb-4" >
    <p>
        El Gobierno Autónomo Municipal de La Paz (GAMLP) es el nombre oficial de la instancia de gobierno del Municipio de La Paz, también conocida como la Alcaldía de La Paz, oficialmente Nuestra Señora de La Paz. Este gobierno alberga, entre otras poblaciones, la ciudad de La Paz, sede del Gobierno de Bolivia.
    </p>
    <p>
        De acuerdo a la Ley de Autonomías y la organización territorial de Bolivia, los municipios bolivianos tienen la potestad de elegir a sus alcaldes en elecciones locales. El GAMLP se compone del poder ejecutivo, representado por el alcalde y su equipo, y el Concejo Municipal, constituido por representantes elegidos mediante voto popular cada 5 años.
    </p>
    <p>
        Tras las elecciones subnacionales de Bolivia de 2021, Iván Arias Durán asumió el cargo de alcalde de La Paz el 5 de mayo de 2021, con un 49.2% de los votos, para el periodo 2021-2025.
    </p>
    <h3>División Política y Administrativa</h3>
    <p>
        El municipio de La Paz se halla dividido en nueve macrodistritos, cada uno bajo la tuición de una subalcaldía y un subalcalde, facilitando la descentralización de algunas actividades administrativas e impositivas.
    </p>
